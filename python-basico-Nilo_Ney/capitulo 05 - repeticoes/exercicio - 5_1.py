'''
Faça um programa que exiba os números de 1 a 100 utilizando:
-> A instrução for
-> A instrução while
'''
print("")

print("Utilizando o laço for ...")
for i in range(1,101):
    print(i)
print()

print("Utilizando o laço while ...")
cont = 1
while cont <= 100:
    print(cont)
    cont = cont + 1
